package com.echevarne.s4dummy.model;

public class BusinessPartner {
    private String BusinessPartner;
    private String FirstName;
    private String LastName;
    private String BusinessPartnerFullName;
    private String BusinessPartnerCategory;
    private String Gender;
    private String Language;
    
	public String getBusinessPartner() {
		return BusinessPartner;
	}
	public void setBusinessPartner(String businessPartner) {
		BusinessPartner = businessPartner;
	}
	public String getFirstName() {
		return FirstName;
	}
	public void setFirstName(String firstName) {
		FirstName = firstName;
	}
	public String getLastName() {
		return LastName;
	}
	public void setLastName(String lastName) {
		LastName = lastName;
	}
	public String getBusinessPartnerFullName() {
		return BusinessPartnerFullName;
	}
	public void setBusinessPartnerFullName(String businessPartnerFullName) {
		BusinessPartnerFullName = businessPartnerFullName;
	}
	public String getBusinessPartnerCategory() {
		return BusinessPartnerCategory;
	}
	public void setBusinessPartnerCategory(String businessPartnerCategory) {
		BusinessPartnerCategory = businessPartnerCategory;
	}
	public String getGender() {
		return Gender;
	}
	public void setGender(String gender) {
		Gender = gender;
	}
	public String getLanguage() {
		return Language;
	}
	public void setLanguage(String language) {
		Language = language;
	}
}
